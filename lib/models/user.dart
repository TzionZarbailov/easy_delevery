class User {
  final String id;
  final String name;
  final String email;
  final String password;
  final String userType;
  final String address;
  final String phoneNumber;

  const User({
    required this.id,
    required this.name,
    required this.email,
    required this.password,
    required this.userType,
    required this.address,
    required this.phoneNumber,
  });
}
